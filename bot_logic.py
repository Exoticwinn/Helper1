def count_down(n: int) -> str:
    """–°—á–∏—Ç–∞–µ—Ç –æ—Ç n –¥–æ 0 —á–µ—Ä–µ–∑ —Å—Ç—Ä–µ–ª–∫—É: n ‚Üí n-1 ‚Üí ... ‚Üí 0"""
